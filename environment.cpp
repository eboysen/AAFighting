SDL_Texture* leftCloud;
SDL_Texture* middleCloud;
SDL_Texture* rightCloud;

environment::environment() {}
environment::environment(SDL_Renderer* render) {
	leftCloud = TextureManager::loadTexture("./assets/leftCloud.png", render);
	middleCloud = TextureManager::loadTexture("./assets/middleCloud.png", render);
	rightCloud = TextureManager::loadTexture("./assets/rightCloud.png", render);
}
environment::~environment() {}

void environment::renderEnvironment(SDL_Renderer* render, int width, int height) {
	HEIGHT = height;
	WIDTH = width;
	platformTiles.clear();
	SDL_Rect rect;
	SDL_Rect srect;
	int size = width / XTILE;
	srect.h = 32;
	srect.w = 32;
	srect.x = 0;
	srect.y = 0;
	rect.h = size;
	rect.w = size;
	for (int x = 0; x < XTILE; x++) {
		for (int y = 0; y < YTILE; y++) {
			rect.x = x * size;
			rect.y = y * size;
			//SDL_SetRenderDrawColor(render, 71, 61, 61, 255);
			//SDL_RenderDrawRect(render, &rect);
			if (environment::level == 0 && omega[y][x] % 2 == 1) {
				position Platform;
				Platform.y = y;
				Platform.x = x;
				platformTiles.push_back(Platform);
				if (omega[y][x] == 1) {
					SDL_RenderCopy(render, leftCloud, NULL, &rect);
				}
				else if (omega[y][x] == 3) {
					SDL_RenderCopy(render, middleCloud, NULL, &rect);
				}
				else if (omega[y][x] == 5) {
					SDL_RenderCopy(render, rightCloud, NULL, &rect);
				}
			}
			else if( alpha[y][x] % 2 == 1) {
				position Platform;
				Platform.y = y;
				Platform.x = x;
				platformTiles.push_back(Platform);
				if (alpha[y][x] == 1) {
					SDL_RenderCopy(render, leftCloud, NULL, &rect);
				}
				else if (alpha[y][x] == 3) {
					SDL_RenderCopy(render, middleCloud, NULL, &rect);
				}
				else if (alpha[y][x] == 5) {
					SDL_RenderCopy(render, rightCloud, NULL, &rect);
				}
			}
		}
	}
}

void environment::platformCheck(entity* Entity) {
	int size = WIDTH / XTILE;
	for (int y = 0; y < platformTiles.size(); y++) {
		if (Entity->isFalling() &&
			Entity->getY() > (double)platformTiles.at(y).y * size &&
			Entity->getY() < ((double)platformTiles.at(y).y + 1) * size) {
			// Right corner Entity to left corner tile
			if (Entity->getX() + Entity->getW() > (double)platformTiles.at(y).x * size &&
				Entity->getX() < (double)platformTiles.at(y).x * size) {
				environment::entityCollided(Entity, (double)platformTiles.at(y).y * size);
			}
			// Left corner Entity to right corner tile
			else if (Entity->getX() < (double)platformTiles.at(y).x * size + size &&
				Entity->getX() + Entity->getW() > (double)platformTiles.at(y).x * size + size) {
				environment::entityCollided(Entity, (double)platformTiles.at(y).y * size);
			}
		}
	}
}

void environment::entityCollided(entity* Entity, double y) {
	Entity->setPlatform(y);
}
