void environment::renderEnvironment(SDL_Renderer * render) {
	
	SDL_Rect rect;
	int WIDTH = 20;
	int HEIGHT = 20;
	rect.h = HEIGHT;
	rect.w = WIDTH;
	rect.x = 0;
	rect.y = 0;
	SDL_SetRenderDrawColor(render, 255, 0, 0, 255);
	SDL_RenderDrawRect(render, &rect);
	for (int x = 0; x < 20; x++) {
		for (int y = 0; y < 25; y++) {
			rect.x = x*WIDTH;
			rect.y = y*HEIGHT;
			if (lvl1[x][y] == 1) {
				SDL_SetRenderDrawColor(render, 0, 0, 0, 255);
			}
			else {
				SDL_SetRenderDrawColor(render, 255,0,0,255);
			}
			SDL_RenderDrawRect(render, &rect);
		}
	}
	

}