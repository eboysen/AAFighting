void environment::renderEnvironment(SDL_Renderer* render, int width, int height) {
	HEIGHT = height;
	WIDTH = width;
	platformTiles.clear();
	SDL_Rect rect;
	int size = width / 25;
	rect.h = size;
	rect.w = size;
	SDL_SetRenderDrawColor(render, 255, 0, 0, 255);
	SDL_RenderDrawRect(render, &rect);
	for (int x = 0; x < YTILE; x++) {
		for (int y = 0; y < XTILE; y++) {
			rect.x = x * size;
			rect.y = y * size;
			if (lvl1[y][x] == 1) {
				SDL_SetRenderDrawColor(render, 21, 21, 21, 255);
				position Platform;
				Platform.y = y;
				Platform.x = x;
				platformTiles.push_back(Platform);
			}
			else {
				SDL_SetRenderDrawColor(render, 71, 31, 31, 255);
			}
			SDL_RenderDrawRect(render, &rect);
		}
	}


}

void environment::platformCheck(character* Character) {
	int size = WIDTH / 25;
	for (int y = 0; y < platformTiles.size(); y++) {
		if (Character->isFalling() &&
			Character->getY() > platformTiles.at(y).y * size &&
			Character->getY() < (platformTiles.at(y).y + 1) * size) {
			// Right corner character to left corner tile
			if (Character->getX() + Character->getW() > platformTiles.at(y).x * size &&
				Character->getX() < platformTiles.at(y).x * size) {
				environment::characterCollided(Character, platformTiles.at(y).y * size);
			}
			// Left corner character to right corner tile
			else if (Character->getX() < platformTiles.at(y).x * size + size &&
				Character->getX() + Character->getW() > platformTiles.at(y).x * size + size) {
				environment::characterCollided(Character, platformTiles.at(y).y * size);
			}
		}
	}
}

void environment::characterCollided(character* Character, double yPos) {
	Character->setPlatform(yPos);
}